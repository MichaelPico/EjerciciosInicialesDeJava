package ejerciciosinicialesdejava;

public class Ej06_ProgramaNumeros1al10v2 {

    public static void main(String[] args) {

        // Programa que atravez de un while te imprime los numeros del 1 al 10
        System.out.println("Programa numeros del 1 al 10");
        System.out.println("----------------------------");
        
        int i=0;
        
        
        //Tipico bucle
        do {            
            i++;
            System.out.println(i);
        } while (i<10);
    }
    
}
